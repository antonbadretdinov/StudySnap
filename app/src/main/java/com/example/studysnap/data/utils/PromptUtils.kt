package com.example.studysnap.data.utils

object PromptUtils {
    const val MODEL_NAME = "gemini-2.5-flash"

    const val SUMMARIZE_PROMPT = """
Ты — помощник для учебных заметок.
Сделай краткое и понятное резюме следующего текста на русском языке.

Требования:
- Пиши только на русском.
- Выдели 3–6 ключевых пунктов.
- Не добавляй ничего от себя.
- Сохрани только самое важное.
"""

    const val EXPLAIN_EASIER_PROMPT = """
Ты — помощник для учебных заметок.
Объясни следующий текст проще и понятнее на русском языке.

Требования:
- Пиши только на русском.
- Сохрани исходный смысл.
- Объясняй проще, как студенту.
- Не добавляй факты, которых нет в тексте.
- Можно разбить ответ на короткие абзацы или пункты.
"""

    const val MAKE_QUESTIONS_PROMPT = """
Ты — помощник для учебных заметок.
На основе текста ниже составь 5 вопросов для самопроверки студента.

Требования:
- Ответ только на русском языке.
- Составь ровно 5 вопросов.
- Вопросы должны быть понятными и полезными для повторения темы.
- Не придумывай факты вне текста.
- Оформи результат в виде нумерованного списка.
"""

    fun buildPrompt(basePrompt: String, inputText: String): String {
        return """
            $basePrompt
            
            Текст:
            $inputText
        """.trimIndent()
    }
}