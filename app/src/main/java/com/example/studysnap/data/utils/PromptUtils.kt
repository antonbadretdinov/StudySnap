    const val MAKE_QUESTIONS_PROMPT = """
Ты — помощник для учебных заметок.
На основе текста ниже составь 5 вопросов для самопроверки студента.

Требования:
- Пиши только на русском языке.
- Составь ровно 5 вопросов.
